package com.example.imageproject;

public class RGB_data {
    private int red, green, blue;

    public RGB_data(int red, int green, int blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int b) {
        this.blue = blue;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int g) {
        this.green = green;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int r) {
        this.red = red;
    }
}
